# Даны два неупорядоченных набора целых чисел (может быть, с
# повторениями). Выдать без повторений в порядке возрастания все те числа, которые
# встречаются в обоих наборах.
# Пользователь вводит 2 числа. n - кол-во элементов первого множества. m - кол-во
# элементов второго множества. Затем пользователь вводит сами элементы множеств.
# 11 6
# 2 4 6 8 10 12 10 8 6 4 2
# 3 6 9 12 15 18
# 6 12

var1 = '11 6' 
var2 = '2 4 6 8 10 12 10 8 6 4 2' 
var3 = '3 6 9 12 15 18' 

# Разбираем первую строку для получения количества элементов в множествах
n, m = map(int, var1.split())

# Преобразуем строки var2 и var3 в множества целых чисел
set1 = set(map(int, var2.split()))
set2 = set(map(int, var3.split()))

# Находим пересечение двух множеств
intersection = set1 & set2

# Преобразуем пересечение в отсортированный список
result = sorted(intersection)

# Выводим результат
print(*result)